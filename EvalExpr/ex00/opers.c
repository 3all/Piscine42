int		ft_plus(int a, int b)
{
	return (a + b);
}

int		ft_minus(int a, int b)
{
	return (a - b);
}

int		ft_mult(int a, int b)
{
	return (a * b);
}

int		ft_div(int a, int b)
{
	return (a / b);
}

int		ft_module(int a, int b)
{
	return (a % b);
}
